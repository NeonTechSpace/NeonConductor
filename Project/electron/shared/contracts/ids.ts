export type EntityId<P extends EntityIdPrefix = EntityIdPrefix> = `${P}_${string}`;

export function isEntityId<P extends EntityIdPrefix>(value: string, prefix: P): value is EntityId<P> {
    return value.startsWith(`${prefix}_`);
}
